import pandas as pd

dados = pd.read_csv('/content/wine.csv')

dados.head(3)

X = dados.iloc[:,0:11].values

X

"""Normalização dos dados"""

from sklearn.preprocessing import StandardScaler

ss = StandardScaler()

ss.fit(X)

X_ss = ss.transform(X)

X_ss

from sklearn.cluster import KMeans

k_means = KMeans(n_clusters = 2)

k_means.fit(X_ss)

centroides = k_means.cluster_centers_
lb_kmeans = k_means.labels_

from sklearn.cluster import DBSCAN

dbscan = DBSCAN(eps = 0.8)

dbscan.fit(X_ss)

lb_scan = dbscan.labels_

sum(lb_kmeans == 0), sum(lb_kmeans == 1)

sum(lb_scan == 0), sum(lb_scan == 1), sum(lb_scan == -1)

from sklearn.decomposition import PCA

pca = PCA(n_components=2)

pca.fit(X_ss)

X_pca = pca.transform(X_ss)

X_pca

dbscan = DBSCAN(eps = 0.8)

dbscan.fit(X_pca)

lb_pca_scan = dbscan.labels_

sum(lb_pca_scan == 0), sum(lb_pca_scan == 1), sum(lb_pca_scan == -1)